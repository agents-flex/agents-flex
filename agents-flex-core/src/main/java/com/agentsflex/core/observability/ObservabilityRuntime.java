package com.agentsflex.core.observability;

import io.opentelemetry.api.OpenTelemetry;
import io.opentelemetry.api.metrics.Meter;
import io.opentelemetry.api.trace.Tracer;

/**
 * A selectable OpenTelemetry runtime used for one logical execution scope.
 *
 * <p>This is an observability abstraction. It is not tied to any Agents-Flex Agent type.</p>
 */
public interface ObservabilityRuntime {
    String getId();

    OpenTelemetry getOpenTelemetry();

    Tracer getTracer();

    Meter getMeter();
}
